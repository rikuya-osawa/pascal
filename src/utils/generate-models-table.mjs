import fs from 'node:fs';
import path from 'node:path';
import matter from 'gray-matter';

const ROOT_DIR = path.resolve('.');
const EN_MODELS_DIR = path.resolve(ROOT_DIR, 'src/content/en/models');
const JA_MODELS_DIR = path.resolve(ROOT_DIR, 'src/content/ja/models');
const OUTPUT_FILE = path.resolve(ROOT_DIR, 'docs/MODELS.md');

const SOURCE_EMOJI = {
  primary: '📚',
  reference: '🔍',
  inspiration: '💡',
};

function listMarkdownFiles(dirPath) {
  if (!fs.existsSync(dirPath)) {
    throw new Error(`Directory not found: ${dirPath}`);
  }

  return fs
    .readdirSync(dirPath, { withFileTypes: true })
    .filter((entry) => entry.isFile() && entry.name.toLowerCase().endsWith('.md'))
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));
}

function readFrontmatterMap(dirPath) {
  const files = listMarkdownFiles(dirPath);
  const result = new Map();

  for (const fileName of files) {
    const fullPath = path.join(dirPath, fileName);
    const raw = fs.readFileSync(fullPath, 'utf-8');
    const parsed = matter(raw);
    const slug = path.basename(fileName, '.md');
    result.set(slug, parsed.data || {});
  }

  return result;
}

function escapeMarkdownCell(value) {
  return String(value)
    .replace(/\r?\n/g, '<br>')
    .replace(/\|/g, '\\|')
    .trim();
}

function formatSourceItem(source) {
  if (!source || typeof source !== 'object') {
    return '';
  }

  const icon = SOURCE_EMOJI[source.sourceType] || '📝';
  const title = typeof source.title === 'string' ? source.title : '';
  const lang = typeof source.sourceLanguage === 'string' && source.sourceLanguage ? source.sourceLanguage : '';

  if (!title) {
    return '';
  }

  return lang ? `${icon} ${title} (${lang})` : `${icon} ${title}`;
}

function formatValue(key, value) {
  if (value == null) {
    return '';
  }

  if (Array.isArray(value)) {
    if (value.length === 0) {
      return '';
    }

    if (key === 'sources') {
      const items = value.map((item) => formatSourceItem(item)).filter(Boolean);
      return items.join(', ');
    }

    const primitiveOnly = value.every(
      (item) => ['string', 'number', 'boolean'].includes(typeof item)
    );

    if (primitiveOnly) {
      return value.map((item) => String(item)).join(', ');
    }

    return JSON.stringify(value);
  }

  if (typeof value === 'object') {
    return JSON.stringify(value);
  }

  return String(value);
}

function collectFrontmatterKeys(enMap, jaMap) {
  const keySet = new Set();

  for (const record of [...enMap.values(), ...jaMap.values()]) {
    for (const key of Object.keys(record)) {
      keySet.add(key);
    }
  }

  return Array.from(keySet).sort((a, b) => a.localeCompare(b));
}

function buildMarkdownTable(enMap, jaMap) {
  const allSlugs = Array.from(new Set([...enMap.keys(), ...jaMap.keys()])).sort((a, b) => a.localeCompare(b));
  const frontmatterKeys = collectFrontmatterKeys(enMap, jaMap);

  const columns = [
    'slug',
    ...frontmatterKeys.map((key) => `EN_${key}`),
    ...frontmatterKeys.map((key) => `JA_${key}`),
  ];

  const lines = [];
  lines.push(`| ${columns.join(' | ')} |`);
  lines.push(`| ${columns.map(() => '---').join(' | ')} |`);

  for (const slug of allSlugs) {
    const enData = enMap.get(slug) || {};
    const jaData = jaMap.get(slug) || {};

    const row = [escapeMarkdownCell(slug)];

    for (const key of frontmatterKeys) {
      row.push(escapeMarkdownCell(formatValue(key, enData[key])));
    }

    for (const key of frontmatterKeys) {
      row.push(escapeMarkdownCell(formatValue(key, jaData[key])));
    }

    lines.push(`| ${row.join(' | ')} |`);
  }

  return lines.join('\n');
}

function buildDocument(tableMarkdown, enCount, jaCount) {
  const generatedAt = new Date().toISOString();

  return [
    '# Models Frontmatter Matrix',
    '',
    `- Generated at: ${generatedAt}`,
    '- Source: src/content/en/models, src/content/ja/models',
    '- This file is auto-generated by src/utils/generate-models-table.mjs',
    `- EN files: ${enCount} / JA files: ${jaCount}`,
    '',
    tableMarkdown,
    '',
  ].join('\n');
}

function main() {
  const enMap = readFrontmatterMap(EN_MODELS_DIR);
  const jaMap = readFrontmatterMap(JA_MODELS_DIR);

  const table = buildMarkdownTable(enMap, jaMap);
  const document = buildDocument(table, enMap.size, jaMap.size);

  fs.writeFileSync(OUTPUT_FILE, document, 'utf-8');
  console.log(`Generated: ${path.relative(ROOT_DIR, OUTPUT_FILE)}`);
}

try {
  main();
} catch (error) {
  console.error('[models-table] Failed to generate docs/MODELS.md');
  console.error(error instanceof Error ? error.message : String(error));
  process.exitCode = 1;
}
